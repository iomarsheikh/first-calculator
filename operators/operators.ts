export function sum(num1: number, num2: number){
    return num1 + num2
}

export function minus(num1: number, num2: number){
    return num1 - num2
}

export function multi(num1: number, num2: number){
    return num1 * num2
}

export function div(num1: number, num2: number){
    return num1 / num2
}